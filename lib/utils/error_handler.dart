  // Generic error handler
  static void handleError(BuildContext context, dynamic error,
      {String? customMessage}) {
    String message = customMessage ?? '‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÑ‡∏°‡πà‡∏ó‡∏£‡∏≤‡∏ö‡∏™‡∏≤‡πÄ‡∏´‡∏ï‡∏∏';

    if (error is FirebaseAuthException) {
      message = handleFirebaseAuthError(error);
    } else if (error is FirebaseException) {
      if (error.plugin == 'cloud_firestore') {
        message = handleFirestoreError(error);
      } else if (error.plugin == 'firebase_storage') {
        message = handleStorageError(error);
      }
    } else if (error is Exception) {
      message = handleNetworkError(error);
    }

    showErrorSnackBar(context, message);
  }

  // Log error for debugging
  static void logError(String operation, dynamic error,
      {StackTrace? stackTrace}) {
    print('üî¥ Error in $operation: $error');
    if (stackTrace != null) {
      print('Stack trace: $stackTrace');
    }
  }

  // Validate form and show errors
  static bool validateAndShowErrors(
    BuildContext context,
    Map<String, String?> validationErrors,
  ) {
    final errors =
        validationErrors.values.where((error) => error != null).toList();

    if (errors.isNotEmpty) {
      showErrorDialog(
        context,
        '‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á',
        errors.join('\n'),
      );
      return false;
    }

    return true;
  }

  // Show confirmation dialog
  static Future<bool> showConfirmationDialog(
    BuildContext context,
    String title,
    String content, {
    String confirmText = '‡∏¢‡∏∑‡∏ô‡∏¢‡∏±‡∏ô',
    String cancelText = '‡∏¢‡∏Å‡πÄ‡∏•‡∏¥‡∏Å',
  }) async {
    final result = await showDialog<bool>(
      context: context,
      builder: (context) => AlertDialog(
        title: Text(title),
        content: Text(content),
        actions: [
          TextButton(
            onPressed: () => Navigator.of(context).pop(false),
            child: Text(cancelText),
          ),
          ElevatedButton(
            onPressed: () => Navigator.of(context).pop(true),
            child: Text(confirmText),
          ),
        ],
      ),
    );

    return result ?? false;
  }

  // Show loading dialog
  static void showLoadingDialog(BuildContext context,
      {String message = '‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÇ‡∏´‡∏•‡∏î...'}) {
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (context) => AlertDialog(
        content: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            const CircularProgressIndicator(),
            const SizedBox(height: 16),
            Text(message),
          ],
        ),
      ),
    );
  }

  // Hide loading dialog
  static void hideLoadingDialog(BuildContext context) {
    Navigator.of(context).pop();
  }
